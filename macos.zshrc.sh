kube-shell() {
  if [[ -z "$1" ]]; then
    echo "Usage: kube-shell <pod_name> [namespace]"
    return 1
  fi

  local pod_name=$1
  local namespace=${2:-default}

  echo "Connecting to Pod '$pod_name' in namespace '$namespace'..."
  kubectl exec -it "$pod_name" -n "$namespace" -- /bin/bash || kubectl exec -it "$pod_name" -n "$namespace" -- /bin/sh
}
kube-find-pod() {
  if [[ -z "$1" ]]; then
    echo "Usage: kube-find-pod <pod_name> [namespace]"
    return 1
  fi

  local pod_name=$1
  local namespace=${2:-default}

  echo "Finding Pod '$pod_name' in namespace '$namespace'..."
  kubectl get pods -n "$namespace" | grep "$pod_name"
}
kube-port-forward() {
  if [[ -z "$1" ]]; then
    echo "Usage: kube-port-forward <pod_name> <local_port> [namespace]"
    return 1
  fi

  local pod_name=$1
  local local_port=$2
  local namespace=${3:-default}

  if [[ -z "$local_port" ]]; then
    local_port=10080
  fi

  echo "Port forwarding from Pod '$pod_name' to local port '$local_port' in namespace '$namespace'..."
  kubectl port-forward "$pod_name" -n "$namespace" "$local_port":80
}
